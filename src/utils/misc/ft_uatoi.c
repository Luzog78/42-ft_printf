#include "ft_printf.h"

int	ft_uatoi(const char *str)
{
	t_signed_size	i;
	int				nbr;

	i = 0;
	nbr = 0;
	while (str[i] >= '0' && str[i] <= '9')
	{
		nbr *= 10;
		nbr += str[i] - '0';
		i++;
	}
	return (nbr);
}
